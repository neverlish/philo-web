const SYMBOLS: [string, string][] = [
  ['소크라테스', '∅'],
  ['플라톤', '◉'],
  ['아리스토텔레스', '⚖'],
  ['데카르트', '✦'],
  ['칸트', '⊞'],
  ['헤겔', '⊗'],
  ['니체', '⚡'],
  ['사르트르', '◈'],
  ['에픽테토스', '⚓'],
  ['프로타고라스', '◎'],
  ['공자', '仁'],
  ['노자', '道'],
  ['붓다', '☸'],
  ['부처', '☸'],
  ['스피노자', '∞'],
  ['흄', '◇'],
  ['로크', '○'],
  ['루소', '♦'],
  ['볼테르', '✧'],
  ['비트겐슈타인', '◳'],
  ['하이데거', '△'],
  ['푸코', '▣'],
  ['보부아르', '❋'],
  ['카뮈', '~'],
  ['마르쿠스 아우렐리우스', '☽'],
  ['아우렐리우스', '☽'],
  ['공리주의', '≈'],
  ['밀', '≈'],
  ['벤담', '≈'],
]

export function getPhilosopherSymbol(name: string): string {
  for (const [key, symbol] of SYMBOLS) {
    if (name.includes(key)) return symbol
  }
  return '◎'
}
